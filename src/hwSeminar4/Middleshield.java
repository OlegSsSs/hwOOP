package hwSeminar4;

public class Middleshield extends Shield{

    public Middleshield(int size, int armorReserve) {
        super(size, armorReserve);
    }

    @Override
    public String toString() {
        return "Middleshield{" +
                "armorReserve=" + armorReserve +
                '}';
    }
}
